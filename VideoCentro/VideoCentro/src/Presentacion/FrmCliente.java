    private void MantenimientobtnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MantenimientobtnGuardarMouseClicked
        int Carnet = 0;
        int Edad = 0;
        
        if (!MantenimientotxtCarnet.getText().equals(""))
        {
            Carnet = Integer.parseInt(MantenimientotxtCarnet.getText());
            Edad = Integer.parseInt(MantenimientotxtEdad.getText());
            
                
            Cliente cliente = new Cliente (
                    Carnet,
                    MantenimientotxtNombre.getText(),
                    MantenimientotxtApellido1.getText(),
                    MantenimientotxtApellido2.getText(),  
                    MantenimientotxtDireccion.getText(),
                    Edad,
                    MantenimientotxtTelefono.getText(),
                    MantenimientotxtFechaNacimiento.getText(),
                    MantenimientotxtCedula.getText(),
                    MantenimientotxtFechaIngreso.getText(),
                    MantenimientotxtEstado.getText());

            mantenimiento.AddElemento(cliente);
            
            ShowTable();
            TabControl.setEnabledAt(0,true);
            TabControl.setEnabledAt(2,true);
            TabControl.setEnabledAt(1,false);
            TabControl.setSelectedIndex(0);
            LimpiarObjetos();
         }
    }//GEN-LAST:event_MantenimientobtnGuardarMouseClicked

