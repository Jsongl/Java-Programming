    private void ShowTable()
    {
        ArrayList<Cliente> lista = mantenimiento.ListadoCliente();
        
        String[] columnNames = {"Carnet","Nombre","Apellido 1","Apellido 2","Direccion","Edad","Telefono","Fecha Nacimiento","Cedula","Fecha de Ingreso","Estado"};
        DefaultTableModel model = new DefaultTableModel ();
        
        model.setColumnIdentifiers (columnNames);
        
        ResultadotblLista.setModel (model);
        
        ResultadotblLista.setForeground(Color.BLACK);
        Font font = new Font ("",1,12);
        ResultadotblLista.setFont(font);
        ResultadotblLista.setRowHeight (30);
        
        for (int i = 0; i < lista.size(); i++)
        {
            int Carnet = lista.get(i).getCarnet();
            String Nombre = lista.get(i).getNombre();
            String Apellido1 = lista.get(i).getApellido1();
            String Apellido2 = lista.get(i).getApellido2();
            String Direccion = lista.get(i).getDireccion();
            int Edad = lista.get(i).getEdad();
            String Telefono = lista.get(i).getTelefono();
            String FechaNacimiento = lista.get(i).getFechaNacimiento();
            String Cedula = lista.get(i).getCedula();
            String FechaIngreso = lista.get(i).getFechaIngreso();
            String Estado = lista.get(i).getEstado();
            
            Object[] data = {Carnet,Nombre,Apellido1,Apellido2,Direccion,Edad,Telefono,FechaNacimiento,Cedula,FechaIngreso,Estado};
            
            model.addRow(data);           
        }        
    }  
    
