    private void MantenimientobtnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MantenimientobtnGuardarMouseClicked
        int CodigoAlquiler = 0;
        int CodigoPelicula = 0;
        
        if (!MantenimientotxtCodigoAlquiler.getText().equals(""))
        {
            CodigoAlquiler = Integer.parseInt(MantenimientotxtCodigoAlquiler.getText());
            CodigoPelicula = Integer.parseInt(MantenimientotxtCodigoPelicula.getText());
            
                
            AlquilerPelicula alquilerpelicula = new AlquilerPelicula (
                    CodigoAlquiler,
                    CodigoPelicula,
                    MantenimientotxtFechaAlquiler.getText(),
                    true,  
                    MantenimientotxtEstado.getText());

            mantenimiento.AddElemento(alquilerpelicula);
            
            ShowTable();
            TabControl.setEnabledAt(0,true);
            TabControl.setEnabledAt(2,true);
            TabControl.setEnabledAt(1,false);
            TabControl.setSelectedIndex(0);
            LimpiarObjetos();
         }
    }//GEN-LAST:event_MantenimientobtnGuardarMouseClicked

