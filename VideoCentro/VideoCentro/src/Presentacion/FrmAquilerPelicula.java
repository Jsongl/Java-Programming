    private void ShowTable()
    {
        ArrayList<AlquilerPelicula> lista = mantenimiento.ListadoAlquilerPelicula();
        
        String[] columnNames = {"Codigo Alquiler","Codigo Pelicula","Fecha Alquiler","Alquilada","Estado"};
        DefaultTableModel model = new DefaultTableModel ();
        
        model.setColumnIdentifiers (columnNames);
        
        ResultadotblLista.setModel (model);
        
        ResultadotblLista.setForeground(Color.BLACK);
        Font font = new Font ("",1,12);
        ResultadotblLista.setFont(font);
        ResultadotblLista.setRowHeight (30);
        
        for (int i = 0; i < lista.size(); i++)
        {
            int CodigoAlquiler = lista.get(i).getCodigoAlquiler();
            int CodigoPelicula = lista.get(i).getCodigoPelicula();
            String FechaAlquiler = lista.get(i).getFechaAlquiler();
            Boolean Alquilada = lista.get(i).getAlquilada();
            String Estado = lista.get(i).getEstado();
            
            Object[] data = {CodigoAlquiler,CodigoPelicula,FechaAlquiler,Alquilada,Estado};
            
            model.addRow(data);           
        }        
    }  
            
